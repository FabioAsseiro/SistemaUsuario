    private void VerificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VerificarActionPerformed
                                           
    try {
    DAO dao = new DAO();
    List<Usuario> usuarios = dao.obterTodosUsuarios();

    if (!usuarios.isEmpty()) {
        String[] columnNames = {"ID", "Nome", "Senha"};
        Object[][] data = new Object[usuarios.size()][3];

        for (int i = 0; i < usuarios.size(); i++) {
            Usuario usuario = usuarios.get(i);
            data[i][0] = usuario.getId();
            data[i][1] = usuario.getNome();
            data[i][2] = usuario.getSenha();
        }

        JTable table = new JTable(data, columnNames);
        table.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        table.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int selectedRow = table.getSelectedRow();

                if (selectedRow != -1) {
                    int id = (int) table.getValueAt(selectedRow, 0);
                    String nome = (String) table.getValueAt(selectedRow, 1);
                    String senha = (String) table.getValueAt(selectedRow, 2);

                    // Atualizar os campos com as informações do usuário selecionado
                    idText.setText(String.valueOf(id));
                    loginText.setText(nome);
                    senhaTxt.setText(senha);
                }
            }
        });

        JScrollPane scrollPane = new JScrollPane(table);
        scrollPane.setPreferredSize(new Dimension(400, 300));

        JOptionPane.showMessageDialog(null, scrollPane, "Usuários", JOptionPane.INFORMATION_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(null, "Não há usuários cadastrados.");
    }
} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Erro ao executar consulta SQL: " + e.getMessage());
}
   
    }//GEN-LAST:event_VerificarActionPerformed

