    private void AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AtualizarActionPerformed
    int id = Integer.parseInt(idText.getText()); // Obtém o ID do usuário a ser atualizado
    String novoNome = loginText.getText(); // Obtém o novo nome digitado
    String novaSenha = senhaTxt.getText(); // Obtém a nova senha digitada
    
    Usuario usuario = new Usuario(id, novoNome, novaSenha);
    
    DAO dao = new DAO();
    try {
        dao.atualizarUsuario(usuario); // Chama o método de atualização no DAO passando o objeto Usuario
        JOptionPane.showMessageDialog(null, "Usuário atualizado com sucesso.");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao atualizar usuário: " + e.getMessage());
    }                                      
     
    }//GEN-LAST:event_AtualizarActionPerformed

